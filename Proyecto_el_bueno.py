def generate_states(graph, availabele_nodes_names):
    nodes_tuples =[]
    node_connection_wieghts = []
    for matrix_row_index in range(len(graph)):
        connections_and_weights = []

        for matrix_column_index in range(len(graph[0])):
            
            if graph[matrix_row_index][matrix_column_index] != 0:
                nodes_tuples.append((availabele_nodes_names[matrix_row_index], availabele_nodes_names[matrix_column_index]))
                connections_and_weights.append(( availabele_nodes_names[matrix_column_index], graph[matrix_row_index][matrix_column_index]))
        
        if len(connections_and_weights) != 0:
            node_connection_wieghts.append((availabele_nodes_names[matrix_row_index], connections_and_weights))

    return (nodes_tuples, node_connection_wieghts)

def check_validity_for_bfs(graph):
    """Checa que todas las conneciones tengan el mismo peso, para que sea valido aplicar el algoritmo de busqueda por anchura"""
    weights = graph[1]
    if weights == []:
        return False
    # Como nos interesa tomar el primer peso posible, lo que vamos a considerar es que los pesos son de la forma
    # weights = [('A', [('B', 1), ...]), ...] asi que necesitamos tomar el elemento 0 de la lista, despues tomar el elemento 1 de la tupla
    # osea, la lista de connecciones con sus pesos, depues, la primera conección y por ultimo tomar el primer peso que esta en la pos 1
    first_weight = weights[0][1][0][1]
    for node_connections in weights:
        for node_weight in node_connections[1]:
            if node_weight[1] != first_weight:
                return False
    return True
    
def bfs(graph, start, goal):
    if start == goal:
        return [start]
    visited = []
    paths = [[start]]
    while paths != []:
        path = paths.pop(0)
        current_node = path[-1]
        if current_node in visited:
            continue
        visited.append(current_node)
        children = [edge[1] for edge in graph if edge[0] == current_node]
        for child in children:
            if child == goal:
                path.append(child)
                return path
            extended_path = path.copy()
            extended_path.append(child)
            paths.append(extended_path)        
    return f"No hay camino entre el nodo: {start} y el nodo: {goal}"

